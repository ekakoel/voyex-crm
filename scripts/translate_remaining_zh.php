$translatePhrase = function(string $text, array $wmap, bool $hans=false) use ($phraseMap, $convertToHans): string {
  $t = trim($text);
  if ($t === '') return $t;
  if (isset($phraseMap[$t])) {
    $out = $phraseMap[$t];
    if ($hans) $out = strtr($out, $convertToHans);
    return $out;
  }
  // Preserve placeholders
  $holders=[];
  $i=0;
  $t = preg_replace_callback('/:[a-zA-Z_]+/', function($m) use (&$holders,&$i){$k='__PH'.$i.'__';$holders[$k]=$m[0];$i++; return $k;}, $t);

  $parts = preg_split('/(\s+|\/|\-|\(|\)|,|\.|:)/u', $t, -1, PREG_SPLIT_DELIM_CAPTURE);
  $out='';
  foreach($parts as $p){
    $lk = strtolower(trim($p));
    if ($lk!=='' && isset($wmap[$lk])) {
      $out .= $wmap[$lk];
    } else {
      $out .= $p;
    }
  }
  foreach($holders as $k=>$v){ $out = str_replace($k,$v,$out); }
  $out = trim(preg_replace('/\s+/', ' ', $out));
  if ($hans) $out = strtr($out, $convertToHans);
  return $out;
};

